clc;
clear all;

Fs=2e9;  																%采样频率 = 2G
N=2e6;    																%采样点数 = 1k
n=0:N-1;t=n/Fs;  														%时间序列（勿动）

% 频率列表, 单位为 MHz
freq_lst = [1, 10, 50, 100, 150, 200, 250, 300, 350, 400, 450, 460, 470, 480, 490, 500, 510, 520, 530, 540, 550, 600, 650, 700, 750, 800];
cnt = length(freq_lst);

for j=1:1:cnt         % 按列表生成
    gen_wave( ...
        0, ...
        Fs, ...
        freq_lst(j) * 1e6, ...
        N, ...
        16384);
end
